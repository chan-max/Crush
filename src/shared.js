function splitStrToArr(target, divider) {
    return target.split(divider);
}
const NATIVE_HTML_TAGS_ARR = splitStrToArr(NATIVE_HTML_TAGS, ',');
const NATIVE_SVG_TAGS_ARR = splitStrToArr(NATIVE_SVG_TAGS, ',');
function isNativeHTMLTag(tagName) {
    return NATIVE_HTML_TAGS_ARR.includes(tagName.toLowerCase());
}
